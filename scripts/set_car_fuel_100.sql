-- ============================================================
-- СКРИПТ: Установить 100% топлива (или 100 л) для машины
-- ============================================================
-- Для электромобилей: 100 = 100% заряда.
-- Для остальных: 100 = 100 л (при необходимости поменяй значение ниже).
-- ============================================================

-- 1. ПРОВЕРКА: Найти машину по гос. номеру
SELECT id, name, plate_number, fuel_level
FROM cars
WHERE plate_number = '058BFF02';  -- <-- ЗАМЕНИ НА НОМЕР

-- 2. ОБНОВЛЕНИЕ: Поставить 100% топлива
UPDATE cars
SET fuel_level = 100
WHERE plate_number = '058BFF02';  -- <-- ЗАМЕНИ НА НОМЕР

-- 3. ПРОВЕРКА: Убедиться, что обновилось
SELECT id, name, plate_number, fuel_level
FROM cars
WHERE plate_number = '058BFF02';  -- <-- ЗАМЕНИ НА НОМЕР
